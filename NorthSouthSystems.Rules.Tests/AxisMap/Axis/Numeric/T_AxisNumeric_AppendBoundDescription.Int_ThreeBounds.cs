    [Fact]
    public void Int_ThreeBounds_Triplets_Mixed()
    {
        Test_Int(["< 2", "2", "> 2"],
            (0, "< 2"),
            (1, "== 2"),
            (2, "> 2")
        );
    }

    [Fact]
    public void Int_ThreeBounds_TwinsTail_Mixed()
    {
        Test_Int(["0", "< 2", "> 2"],
            (0, "== 0"),
            (1, "== 1"),
            (2, "> 2")
        );

        Test_Int(["< 0", "< 2", "> 2"],
            (0, "< 0"),
            (1, "[0, 1]"),
            (2, "> 2")
        );

        Test_Int(["<= 0", "< 2", "> 2"],
            (0, "<= 0"),
            (1, "== 1"),
            (2, "> 2")
        );

        Test_Int(["< 1", "< 2", "> 2"],
            (0, "< 1"),
            (1, "== 1"),
            (2, "> 2")
        );

        //Test_Int(["1", "<= 2", "> 2"] OverlappingOneOff.

        Test_Int(["< 1", "<= 2", "> 2"],
            (0, "< 1"),
            (1, "[1, 2]"),
            (2, "> 2")
        );

        Test_Int(["<= 1", "<= 2", "> 2"],
            (0, "<= 1"),
            (1, "== 2"),
            (2, "> 2")
        );

        Test_Int(["0", "<= 2", "> 2"],
            (0, "== 0"),
            (1, "[1, 2]"),
            (2, "> 2")
        );

        Test_Int(["< 0", "<= 2", "> 2"],
            (0, "< 0"),
            (1, "[0, 2]"),
            (2, "> 2")
        );

        Test_Int(["<= 0", "<= 2", "> 2"],
            (0, "<= 0"),
            (1, "[1, 2]"),
            (2, "> 2")
        );
    }

