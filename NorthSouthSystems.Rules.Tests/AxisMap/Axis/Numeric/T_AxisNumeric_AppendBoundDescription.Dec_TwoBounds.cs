public partial class T_AxisNumeric_AppendBoundDescription
{
    [Fact]
    public void Dec_TwoBounds_ScaleMixed()
    {
        Test_Dec(["-2", "-3.0"],
            (0, "== -2.0"),
            (1, "== -3.0")
        );

        Test_Dec(["2", "3.0"],
            (0, "== 2.0"),
            (1, "== 3.0")
        );

        Test_Dec(["-3.0", "-2"],
            (0, "== -3.0"),
            (1, "== -2.0")
        );

        Test_Dec(["3.0", "2"],
            (0, "== 3.0"),
            (1, "== 2.0")
        );

        Test_Dec(["-2", "-3.00"],
            (0, "== -2.00"),
            (1, "== -3.00")
        );

        Test_Dec(["2", "3.00"],
            (0, "== 2.00"),
            (1, "== 3.00")
        );

        Test_Dec(["-3.00", "-2"],
            (0, "== -3.00"),
            (1, "== -2.00")
        );

        Test_Dec(["3.00", "2"],
            (0, "== 3.00"),
            (1, "== 2.00")
        );
    }

    [Fact]
    public void Dec_TwoBounds_Twins()
    {
        Test_Dec(["2.0", "> 2.0"],
            (0, "== 2.0"),
            (1, "> 2.0")
        );

        Test_Dec(["< 2.0", "2.0"],
            (0, "< 2.0"),
            (1, "== 2.0")
        );
    }

    [Fact]
    public void Dec_TwoBounds_OneOff()
    {
        Test_Dec(["2.0", "3.0"],
            (0, "== 2.0"),
            (1, "== 3.0")
        );

        Test_Dec(["2.0", "> 3.0"],
            (0, "== 2.0"),
            (1, "> 3.0")
        );

        Test_Dec(["2.0", ">= 3.0"],
            (0, "== 2.0"),
            (1, ">= 3.0")
        );

        Test_Dec(["< 2.0", "3.0"],
            (0, "< 2.0"),
            (1, "== 3.0")
        );

        Test_Dec(["<= 2.0", "3.0"],
            (0, "<= 2.0"),
            (1, "== 3.0")
        );

        Test_Dec(["> 2.0", "> 3.0"],
            (0, "(2.0, 3.0]"),
            (1, "> 3.0")
        );

        Test_Dec(["< 2.0", "< 3.0"],
            (0, "< 2.0"),
            (1, "[2.0, 3.0)")
        );
    }

